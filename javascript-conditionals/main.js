function categorizeAcidity(ph) {
  if (ph === 7) {
    return 'Neutral';
  } else if (ph < 7 && ph >= 0) {
    return 'acid';
  } else if (ph > 7 && ph <= 14) {
    return 'base';
  } else {
    return 'invalid pH level';
  }
}

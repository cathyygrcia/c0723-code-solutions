function recommendMovie(genre) {
  switch (genre) {
    case 'action':
      return 'Die Hard';
    case 'comedy':
      return 'The Big Lebowski';
    case 'musical':
      return 'Wicked';
    case 'drama':
      return 'A Star is Born';
    case 'Horror':
      return 'Insidious';
    case 'Sci-fi':
      return 'Interstellar';
    default:
      return 'Genre not recognized. Choose between action, comedy, horror, drama, musical, or sci-fi';
  }
}
